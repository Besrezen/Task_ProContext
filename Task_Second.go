package main

import (
	"bufio"
	"fmt"
	"os"
	"strings"
)

func main() {
	/*
	   В условии задачи:
	   - Есть три автомата с наклейками:
	     1. "Красная"
	     2. "Красная и зелёная"
	     3. "Зелёная"
	   - При этом:
	     * Один выдаёт только красную жвачку.
	     * Один выдаёт только зелёную жвачку.
	     * Один выдаёт смесь красной и зелёной.
	   - Наклейки на автоматах все неверны.
	   Решение:
	   Выбираем автомат с наклейкой "Красная и зелёная". Так как его наклейка неверна,
	   он не может выдавать и красную, и зелёную одновременно. Он выдаёт либо только красную,
	   либо только зелёную.
	   Далее:
	   - Если купленная жвачка оказалась красной, то:
	         Автомат "Красная и зелёная" выдаёт красную.
	         Автомат с наклейкой "Красная" не может выдавать красную, значит, он выдаёт только зелёную.
	         Остаётся автомат с наклейкой "Зелёная", который выдаёт смесь.
	   - Если жвачка зелёная, то:
	         Автомат "Красная и зелёная" выдаёт зелёную.
	         Автомат с наклейкой "Зелёная" не может выдавать зелёную, значит, он выдаёт только красную.
	         Остаётся автомат с наклейкой "Красная", который выдаёт смесь.
	*/

	reader := bufio.NewReader(os.Stdin)
	fmt.Println("Купите одну жвачку из автомата с наклейкой 'Красная и зелёная'.")
	fmt.Print("Введите цвет полученной жвачки ('красная' или 'зелёная'): ")
	input, _ := reader.ReadString('\n')
	color := strings.ToLower(strings.TrimSpace(input))

	if color != "красная" && color != "зелёная" {
		fmt.Println("Неверный ввод. Запустите программу снова и введите 'красная' или 'зелёная'.")
		return
	}

	fmt.Println("\nРезультаты анализа:")
	if color == "красная" {
		fmt.Println("Автомат с наклейкой 'Красная и зелёная' продаёт: только красную жвачку.")
		fmt.Println("Автомат с наклейкой 'Красная' продаёт: только зелёную жвачку.")
		fmt.Println("Автомат с наклейкой 'Зелёная' продаёт: смесь красной и зелёной жвачки.")
	} else { // зелёная
		fmt.Println("Автомат с наклейкой 'Красная и зелёная' продаёт: только зелёную жвачку.")
		fmt.Println("Автомат с наклейкой 'Зелёная' продаёт: только красную жвачку.")
		fmt.Println("Автомат с наклейкой 'Красная' продаёт: смесь красной и зелёной жвачки.")
	}
}
